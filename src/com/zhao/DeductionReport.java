package com.zhao;

public class DeductionReport {

    private String year;

    private String startingSalary;

    private String numofDeductions;

    private String deductionPercent;

    private String deductionAmount;

    public String getYear() {
        return year;
    }

    public void setYear(String year) {
        this.year = year;
    }

    public String getStartingSalary() {
        return startingSalary;
    }

    public void setStartingSalary(String startingSalary) {
        this.startingSalary = startingSalary;
    }

    public String getNumofDeductions() {
        return numofDeductions;
    }

    public void setNumofDeductions(String numofDeductions) {
        this.numofDeductions = numofDeductions;
    }

    public String getDeductionPercent() {
        return deductionPercent;
    }

    public void setDeductionPercent(String deductionPercent) {
        this.deductionPercent = deductionPercent;
    }

    public String getDeductionAmount() {
        return deductionAmount;
    }

    public void setDeductionAmount(String deductionAmount) {
        this.deductionAmount = deductionAmount;
    }
}
