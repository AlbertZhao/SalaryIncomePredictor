package com.zhao;

public class IncrementReport {
    private String year;

    private String startingSalary;

    private String numofIncrements;

    private String incrementPercent;

    private String incrementAmount;

    public String getYear() {
        return year;
    }

    public void setYear(String year) {
        this.year = year;
    }

    public String getStartingSalary() {
        return startingSalary;
    }

    public void setStartingSalary(String startingSalary) {
        this.startingSalary = startingSalary;
    }

    public String getNumofIncrements() {
        return numofIncrements;
    }

    public void setNumofIncrements(String numofIncrements) {
        this.numofIncrements = numofIncrements;
    }

    public String getIncrementPercent() {
        return incrementPercent;
    }

    public void setIncrementPercent(String incrementPercent) {
        this.incrementPercent = incrementPercent;
    }

    public String getIncrementAmount() {
        return incrementAmount;
    }

    public void setIncrementAmount(String incrementAmount) {
        this.incrementAmount = incrementAmount;
    }
}
